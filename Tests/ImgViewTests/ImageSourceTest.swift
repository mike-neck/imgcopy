import imgview
import XCTest
import IOUtils

class ImageSourceTest: XCTestCase {

    struct TestInitTestCase {
        let source: String?
        let expected: ImageSource
        let msg: String
    }

    func testInit() throws {
        let tests: [TestInitTestCase] = [
            TestInitTestCase(
                    source: nil,
                    expected: .clipboard,
                    msg: "ImageSource with Nil source becomes .clipboard",
            ),
            TestInitTestCase(
                source: "",
                expected: .clipboard,
                msg: "ImageSource with EMPTY source becomes .clipboard",
            ),
            TestInitTestCase(
                source: "test-data/1x1.png",
                expected: .file(content: FileContent.png(Data([
                    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
                    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x01, 0x03, 0x00, 0x00, 0x00, 0x25, 0xDB, 0x56,
                    0xCA, 0x00, 0x00, 0x00, 0x03, 0x50, 0x4C, 0x54, 0x45, 0x00, 0x00, 0x00, 0xA7, 0x7A, 0x3D, 0xDA,
                    0x00, 0x00, 0x00, 0x01, 0x74, 0x52, 0x4E, 0x53, 0x00, 0x40, 0xE6, 0xD8, 0x66, 0x00, 0x00, 0x00,
                    0x0A, 0x49, 0x44, 0x41, 0x54, 0x08, 0xD7, 0x63, 0x60, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01, 0xE2,
                    0x21, 0xBC, 0x33, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
                ]))),
                msg: "ImageSource with valid source becomes .file",
            ),
        ]
        for test in tests {
            let imageSource = ImageSource(test.source)
            XCTAssertEqual(imageSource, test.expected, test.msg)
        }
    }

    @MainActor
    static let allTests = [
        ("testInit", testInit)
    ]
}
